# Definindo o valor do índice da sequência
INDICE = 12

# Definindo o valor inicial da soma
SOMA = 0

# Definindo o valor inicial do índice da sequência
K = 1

# Fazendo o laço de repetição que vai de 1 até o valor do índice
while K < INDICE:
    # Incrementando o índice da sequência
    K += 1

    # Somando o valor do índice atual à variável SOMA
    SOMA += K

# Imprime o valor da soma
print(SOMA)

# Valor da soma = 77